package com.example.application.clases;

class Empleado {
    String nombre;
    int cedula;
    int edad;
    String correo;

    public Empleado(String nombre, int cedula, int edad, String correo) {
        this.nombre = nombre;
        this.cedula = cedula;
        this.edad = edad;
        this.correo = correo;
    }
}
