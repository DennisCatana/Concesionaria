package com.example.application.clases;

class Persona {
    String nombre;
    int cedula;
    int edad;
    String correo;

    public Persona() {
        this.nombre = nombre;
        this.cedula = cedula;
        this.edad = edad;
        this.correo = correo;
    }

    public int getCedula() {
        return cedula;
    }
}
